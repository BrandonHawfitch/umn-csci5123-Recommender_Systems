      if not os.path.exists(group_file_path):
        break  # Stop if no more folds exist for this group_file


      # Load groups for the current fold
      with open(group_file_path, 'r') as file:
        groups = json.load(file)

      utility_function = utility_functions[utility_name]
      fairness_function = fairness_functions[fairness_name]
      algorithm = search_algorithms[search_algorithm]

      experiment_name = f"{group_file_name}_fold_{fold_index}_list-size-{list_size}_{utility_name}_{fairness_name}_{lam}_{search_algorithm}"

      output_files = os.listdir("output")
