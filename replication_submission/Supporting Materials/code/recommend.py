def get_recs(args):
    group, algorithm, items, list_size, fairness_function, utility_function, lam = args #unpack args
    recommendations = algorithm(group, items, list_size, fairness_function, utility_function, lam)
    group.sort(key=lambda x: int(x))
    group_id = ",".join(map(str, group))
    return (group_id, recommendations)

