"""## Relevance Calculation

Relevance: Essentialy scales user rating on a 0 to 1 scale with a 0 meaning item is irrelevant to user and 1 meaning the item is very relevant
"""

# Calculate relevance matrix

# In group recommendations, we first compute relevance of candidate items to users
# This produces a full user-item interaction matrix where each entry represents pairwise relevance
# This is typically computed as r_ui / r_max, or a user-item rating divided by the maximum possible rating
r_max = 5.0
r_min = 0.5
rel_min = r_min / r_max
rel_max = 1.0

# calculates relevance for single user-item pair interaction
def get_relevance(rating):
  if np.isnan(rating): return 0.0
  rel = rating / r_max
  return max(rel_min, min(rel_max, rel)) # Clamps value between rel_min and rel_max

relevance_matrix = rating_matrix.map(get_relevance)

"""## Calculations

### Utility Calculations

Ulility: Measures how satisfied a user is with a list of items

Proportional Utility: Meausres how satisfied a user is with a list of items relative to their own top-k list
"""

# Utility calculations, under definition 3.1

# Average utility
def utility_a(user: str, recommendations: list[str]) -> int:
  list_size = len(recommendations) # K, in the paper
  rel_sum = int(relevance_matrix.loc[user, recommendations].sum())
  utility = rel_sum / (list_size * rel_max)
  return utility

# Proportionality utility
def utility_p(user: str, recommendations: list[str]) -> int:
  list_size = len(recommendations) # K, in the paper

  rel_sum = relevance_matrix.loc[user, recommendations].sum()

  # Get the user's top list_size favorite items
  user_ratings = rating_matrix.loc[user].sort_values(ascending=False)
  top_items = user_ratings.index[:list_size]

  denominator = relevance_matrix.loc[user, top_items].loc[top_items.isin(recommendations)].sum()
  if denominator == 0: return 0.0 # Handle zero denominator to avoid division by zero

  utility = rel_sum / denominator
  return utility

"""### Social Welfare Calculation"""

# Social welfare describes the aggregation of individual utilities within a group
# AKA the average satisfaction with recommended list within a group
# Via the paper, this is stated under Definition 3.2

def social_welfare(group, recommendations, utility_function):
  group_size = len(group)
  welfare_sum = 0.0
  for user in group:
    welfare_sum += utility_function(user, recommendations)
  return welfare_sum / group_size

"""### Fairness"""

# Fairness depicts how imbalanced user satisfaction is
# Via the paper, this is stated under Definition 3.3

# Least Misery: Worst case user satisfaction
def fairness_lm(group: list[str], recommendations: list[str], utility_function):
  utilities = [utility_function(user, recommendations) for user in group]
  return min(utilities)

# Variance: How uneven the utilities are across group members
def fairness_var(group: list[str], recommendations: list[str], utility_function):
  utilities = [utility_function(user, recommendations) for user in group]
  variance = np.var(utilities)
  return 1 - variance

# Jain's: Are users getting a fairshare of satisfaction
def fairness_j(group: list[str], recommendations: list[str], utility_function):
  utilities = [utility_function(user, recommendations) for user in group]

  numerator = np.sum(utilities)**2
  denominator = len(utilities) * np.sum([u**2 for u in utilities])

  if denominator == 0: return 0.0 # Handle zero denominator to avoid division by zero

  fairness = numerator / denominator
  return fairness

# Min-Max Ratio: ratio between most and least satisfied user
def fairness_m(group: list[str], recommendations: list[str], utility_function):
  utilities = [utility_function(user, recommendations) for user in group]

  # Min-Max Ratio calculation
  min_utility = min(utilities)
  max_utility = max(utilities)

  if max_utility == 0: return 0.0 # Handle zero denominator to avoid division by zero

  fairness = min_utility / max_utility
  return fairness

"""## Algorithms"""

# The primary algorithms for outputting recommendation lists

# The Greedy algorithm
def greedy_algorithm(group: list[str], items: list[str], list_length: int, fairness_function, utility_function, lam: float):
  recommendations = []
  iteration = 0
  while len(recommendations) < list_length:
    iteration += 1

    best_item = None
    best_score = -1  # Initialize with a low score

    for item in items:
      if item not in recommendations:  # Avoid adding the same item multiple times
        candidate_recommendations = recommendations + [item]

        # Calculate social welfare and fairness for candidate recommendations
        sw = social_welfare(group, candidate_recommendations, utility_function)
        fairness = fairness_function(group, candidate_recommendations, utility_function)

        # Calculate combined score
        score = lam * sw + (1 - lam) * fairness

        # Update best_item and best_score if current item has a higher score
        if score > best_score:
          best_score = score
          best_item = item

    if best_item is not None:
      recommendations.append(best_item)
    else:
      break  # Break if no suitable item is found

  return recommendations

"""## Evaluation Metrics"""

import math

def evaluate(group, recommendations, test_matrix, k=10):
  precision_scores = []
  recall_scores = []
  f_scores = []
  ndcg_scores = []


  for user in group:

    if user not in test_matrix.index:
      print(f"User {user} not found in test_matrix.")
      continue
    #need to get true items from user's in the test set
    true_items = set(test_matrix.loc[user].dropna().index)

    #if user has no test data skip user
    if not true_items:
      print(f"User {user} has no test items.")
      continue

    recommended_items = recommendations[:k]
    hits = [1 if item in true_items else 0 for item in recommended_items]


    precision = sum(hits)/k
    recall = sum(hits)/ min(k, len(true_items))
    if precision + recall == 0:
        f1 = 0
    else:
        f1 = 2 * precision * recall / (precision + recall)


    dcg = sum([hit / math.log2(i + 2) for i, hit in enumerate(hits)])
    idcg = sum([1 / math.log2(i + 2) for i in range(min(len(true_items), k))])
    ndcg = dcg / idcg if idcg > 0 else 0

    precision_scores.append(precision)
    recall_scores.append(recall)
    f_scores.append(f1)
    ndcg_scores.append(ndcg)

  return {
      'precision': np.mean(precision_scores),
      'recall': np.mean(recall_scores),
      'f1': np.mean(f_scores),
      'ndcg': np.mean(ndcg_scores)
  }

